// Package gen renders a spec.Command tree into completion scripts for
// bash, zsh and fish. Each shell has its own file; this file holds the
// shared plumbing: node enumeration, identifier sanitizing, quoting and
// the generated-file header.
//
// Output is fully deterministic: nodes are emitted in tree order and
// choices are sorted, so regenerating over the same help text yields a
// byte-identical script (good for committing completions to a repo).
package gen

import (
	"fmt"
	"regexp"
	"strings"

	"github.com/JaydenCJ/tabsmith/internal/spec"
	"github.com/JaydenCJ/tabsmith/internal/version"
)

// Shell names one of the supported target dialects.
type Shell string

// The supported shells.
const (
	Bash Shell = "bash"
	Zsh  Shell = "zsh"
	Fish Shell = "fish"
)

// Shells lists every supported target in canonical order.
var Shells = []Shell{Bash, Zsh, Fish}

// ParseShell validates a user-supplied shell name.
func ParseShell(s string) (Shell, error) {
	switch Shell(strings.ToLower(s)) {
	case Bash:
		return Bash, nil
	case Zsh:
		return Zsh, nil
	case Fish:
		return Fish, nil
	}
	return "", fmt.Errorf("unknown shell %q (expected bash, zsh or fish)", s)
}

// Generate renders the completion script for one shell.
func Generate(shell Shell, cmd *spec.Command) (string, error) {
	switch shell {
	case Bash:
		return generateBash(cmd), nil
	case Zsh:
		return generateZsh(cmd), nil
	case Fish:
		return generateFish(cmd), nil
	}
	return "", fmt.Errorf("unknown shell %q", shell)
}

// FileName returns the conventional install name for a script:
// tool.bash, _tool (zsh's $fpath convention) and tool.fish.
func FileName(shell Shell, tool string) string {
	switch shell {
	case Zsh:
		return "_" + tool
	case Fish:
		return tool + ".fish"
	default:
		return tool + ".bash"
	}
}

// node is one command in the flattened tree, with a stable dotted id
// ("root", "root.add", "root.add.remote") and the subcommand path.
type node struct {
	id   string
	path []string
	cmd  *spec.Command
}

// collectNodes flattens the tree depth-first, matching help-screen order.
func collectNodes(root *spec.Command) []node {
	var out []node
	root.Walk(func(path []*spec.Command) {
		id := "root"
		var names []string
		for _, c := range path[1:] {
			names = append(names, c.Name)
			id += "." + c.Name
		}
		out = append(out, node{id: id, path: names, cmd: path[len(path)-1]})
	})
	return out
}

var unsafeIdent = regexp.MustCompile(`[^A-Za-z0-9_]`)

// ident turns an arbitrary tool or path element into a safe shell
// function-name fragment.
func ident(s string) string {
	s = unsafeIdent.ReplaceAllString(s, "_")
	if s == "" {
		s = "_"
	}
	return s
}

// funcName builds the per-node completion function name for a shell.
func funcName(tool string, path []string) string {
	parts := []string{"_tabsmith", ident(tool)}
	for _, p := range path {
		parts = append(parts, ident(p))
	}
	return strings.Join(parts, "_")
}

// shQuote single-quotes s for POSIX-family shells (bash, zsh, fish all
// accept the '\” splice).
func shQuote(s string) string {
	return "'" + strings.ReplaceAll(s, "'", `'\''`) + "'"
}

// header renders the shared comment banner for a generated script.
func header(shell Shell, tool string) string {
	var b strings.Builder
	fmt.Fprintf(&b, "# %s completion for %s — generated by tabsmith v%s\n",
		shell, tool, version.Version)
	fmt.Fprintf(&b, "# from `%s --help`. Regenerate: tabsmith gen --shell %s %s\n",
		tool, shell, tool)
	b.WriteString("# https://github.com/JaydenCJ/tabsmith\n")
	return b.String()
}

// flagForms returns every spelling of every flag of cmd, in listing order.
func flagForms(cmd *spec.Command) []string {
	var out []string
	for i := range cmd.Flags {
		out = append(out, cmd.Flags[i].Forms()...)
	}
	return out
}

// subNames returns the names of cmd's direct subcommands, in listing order.
func subNames(cmd *spec.Command) []string {
	var out []string
	for _, s := range cmd.Subcommands {
		out = append(out, s.Name)
	}
	return out
}

// positionalChoices unions the literal choices of cmd's positionals.
func positionalChoices(cmd *spec.Command) []string {
	var out []string
	for _, p := range cmd.Positionals {
		out = append(out, p.Choices...)
	}
	return out
}

// positionalHint returns the first file/dir hint among cmd's positionals.
func positionalHint(cmd *spec.Command) spec.ValueHint {
	for _, p := range cmd.Positionals {
		if p.Hint != spec.HintNone {
			return p.Hint
		}
	}
	return spec.HintNone
}

// valueFlags returns the flags of cmd that require a separate-word value
// (optional-argument flags like --color[=WHEN] are attached-only, so they
// must not swallow the next word).
func valueFlags(cmd *spec.Command) []*spec.Flag {
	var out []*spec.Flag
	for i := range cmd.Flags {
		f := &cmd.Flags[i]
		if f.TakesValue() && !f.ArgOptional {
			out = append(out, f)
		}
	}
	return out
}
