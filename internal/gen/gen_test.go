// Tests for the shared generator plumbing and cross-shell properties:
// deterministic output, file naming and identifier sanitizing.
package gen

import (
	"strings"
	"testing"

	"github.com/JaydenCJ/tabsmith/internal/spec"
)

// sample builds the tree used across the generator tests: a root with a
// choice flag, a file flag and two subcommands, one nested one deeper.
func sample() *spec.Command {
	root := &spec.Command{
		Name: "ship",
		Flags: []spec.Flag{
			{Short: "-v", Long: "--verbose", Desc: "print progress"},
			{Long: "--color", Arg: "WHEN", ArgOptional: true,
				Choices: []string{"always", "never", "auto"}},
			{Short: "-c", Long: "--config", Arg: "FILE", Hint: spec.HintFile,
				Desc: "config file to load"},
		},
	}
	deploy := root.AddSubcommand(&spec.Command{
		Name: "deploy", Summary: "roll a release out",
		Flags: []spec.Flag{
			{Long: "--strategy", Arg: "KIND",
				Choices: []string{"rolling", "canary"}, Desc: "how to roll"},
		},
	})
	deploy.AddSubcommand(&spec.Command{
		Name: "history", Summary: "list past deployments",
		Flags: []spec.Flag{{Long: "--json", Desc: "machine output"}},
	})
	root.AddSubcommand(&spec.Command{
		Name: "logs", Summary: "tail logs",
		Positionals: []spec.Positional{{Name: "FILE", Hint: spec.HintFile}},
	})
	return root
}

func TestShellNamesAndFileConventions(t *testing.T) {
	for _, name := range []string{"bash", "ZSH", "Fish"} {
		if _, err := ParseShell(name); err != nil {
			t.Fatalf("%s: %v", name, err)
		}
	}
	if _, err := ParseShell("powershell"); err == nil {
		t.Fatal("unknown shell must error")
	}
	if FileName(Bash, "x") != "x.bash" || FileName(Zsh, "x") != "_x" ||
		FileName(Fish, "x") != "x.fish" {
		t.Fatal("unexpected file names")
	}
}

func TestGenerateIsDeterministicAcrossShells(t *testing.T) {
	for _, sh := range Shells {
		a, err := Generate(sh, sample())
		if err != nil {
			t.Fatal(err)
		}
		b, _ := Generate(sh, sample())
		if a != b {
			t.Fatalf("%s output is not byte-identical across runs", sh)
		}
	}
}

func TestGeneratedHeadersNameToolAndVersion(t *testing.T) {
	for _, sh := range Shells {
		out, _ := Generate(sh, sample())
		if !strings.Contains(out, "generated by tabsmith v0.1.0") {
			t.Fatalf("%s: missing version header", sh)
		}
		if !strings.Contains(out, "`ship --help`") {
			t.Fatalf("%s: missing provenance line", sh)
		}
	}
}

func TestIdentifierAndQuotingHelpers(t *testing.T) {
	// Hostile tool names must sanitize into legal function names, path
	// encoding must be stable, and quoting must splice single quotes.
	if got := ident("my-tool.v2"); got != "my_tool_v2" {
		t.Fatalf("got %q", got)
	}
	if got := ident(""); got != "_" {
		t.Fatalf("got %q", got)
	}
	if got := funcName("ship", []string{"deploy", "history"}); got != "_tabsmith_ship_deploy_history" {
		t.Fatalf("got %q", got)
	}
	if got := shQuote("it's"); got != `'it'\''s'` {
		t.Fatalf("got %q", got)
	}
}

func TestValueFlagsExcludesOptionalArgFlags(t *testing.T) {
	// --color[=WHEN] must not swallow the next word, so it is not a
	// separate-word value flag.
	vf := valueFlags(sample())
	if len(vf) != 1 || vf[0].Long != "--config" {
		t.Fatalf("got %+v", vf)
	}
}
